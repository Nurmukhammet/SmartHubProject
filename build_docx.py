def para(text, indent=True):
    p = doc.add_paragraph()
    if indent:
        p.paragraph_format.first_line_indent = Mm(12.5)
    for chunk, bold in _bold_split(text):
        run = p.add_run(chunk); run.bold = bold
    return p


def _bold_split(text):
    # **жуан** үзінділерді бөлу
    parts = []
    i = 0
    while True:
        s = text.find('**', i)
        if s == -1:
            parts.append((text[i:], False)); break
        parts.append((text[i:s], False))
        e = text.find('**', s + 2)
        if e == -1:
            parts.append((text[s:], False)); break
        parts.append((text[s+2:e], True)); i = e + 2
    return [(c, b) for c, b in parts if c]


def bullet(items, numbered=False):
    style = 'List Number' if numbered else 'List Bullet'
    for it in items:
        p = doc.add_paragraph(style=style)
        p.paragraph_format.line_spacing_rule = WD_LINE_SPACING.ONE_POINT_FIVE
        for chunk, bold in _bold_split(it):
            r = p.add_run(chunk); r.bold = bold; r.font.size = Pt(14)


def code(text):
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(4); p.paragraph_format.space_after = Pt(8)
    p.paragraph_format.line_spacing_rule = WD_LINE_SPACING.SINGLE
    p.alignment = WD_ALIGN_PARAGRAPH.LEFT
    p.paragraph_format.left_indent = Mm(2)
    _shade(p, 'F2F2F2')
    r = p.add_run(text); r.font.name = 'Courier New'; r.font.size = Pt(9.5)
    r._element.rPr.rFonts.set(qn('w:eastAsia'), 'Courier New')
    return p


def figure(path, caption, width_mm=120):
    if os.path.exists(os.path.join(DIR, path)):
        p = doc.add_paragraph(); p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        try:
            p.add_run().add_picture(os.path.join(DIR, path), width=Mm(width_mm))
        except Exception:
            p.add_run('[сурет]')
        c = doc.add_paragraph(); c.alignment = WD_ALIGN_PARAGRAPH.CENTER
        rc = c.add_run(caption); rc.italic = True; rc.font.size = Pt(13)


def table(headers, rows, caption=None):
    if caption:
        cp = doc.add_paragraph(); cp.alignment = WD_ALIGN_PARAGRAPH.RIGHT
        rc = cp.add_run(caption); rc.italic = True; rc.font.size = Pt(13)
    t = doc.add_table(rows=1, cols=len(headers)); t.style = 'Table Grid'
    t.alignment = WD_TABLE_ALIGNMENT.CENTER
    for i, htext in enumerate(headers):
        cell = t.rows[0].cells[i]; cell.paragraphs[0].text = ''
        r = cell.paragraphs[0].add_run(htext); r.bold = True; r.font.size = Pt(13)
    for row in rows:
        cells = t.add_row().cells
        for i, val in enumerate(row):
            cells[i].paragraphs[0].text = ''
            r = cells[i].paragraphs[0].add_run(str(val)); r.font.size = Pt(13)


def center(text, bold=False, size=14):
    p = doc.add_paragraph(); p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    p.paragraph_format.first_line_indent = Mm(0)
    r = p.add_run(text); r.bold = bold; r.font.size = Pt(size)
    return p

