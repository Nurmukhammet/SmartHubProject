def _shade(par, color='F2F2F2'):
    sh = OxmlElement('w:shd'); sh.set(qn('w:val'), 'clear')
    sh.set(qn('w:fill'), color)
    par.paragraph_format.element.get_or_add_pPr().append(sh)


def h1(text):
    doc.add_page_break()
    p = doc.add_paragraph()
