// ── Реализация переключателя ──
(function initI18n() {
  const norm = (s) => (s || '').replace(/\s+/g, ' ').trim();

  // Элементы с переводимым текстом (только листовые — без вложенных тегов)
  const SEL = 'h1,h2,h3,h4,p,span,a,button,li,figcaption,option,.billboard-title,.billboard-chip,.level-name,.level-age,.report-tag';
  const nodes = [];
  document.querySelectorAll(SEL).forEach((el) => {
    if (el.children.length === 0) {
      const ru = norm(el.textContent);
      if (ru) { el.dataset.ru = ru; nodes.push(el); }
    }
  });

  // Плейсхолдеры полей формы
  const phNodes = [];
  document.querySelectorAll('input[placeholder],textarea[placeholder]').forEach((el) => {
    el.dataset.ruPh = el.getAttribute('placeholder');
    phNodes.push(el);
  });

  function apply(lang) {
    const dict = TR[lang] || {};
    nodes.forEach((el) => {
      const ru = el.dataset.ru;
      el.textContent = (lang === 'ru') ? ru : (dict[ru] || ru);
    });
    phNodes.forEach((el) => {
      const ru = el.dataset.ruPh;
      el.setAttribute('placeholder', (lang === 'ru') ? ru : (dict[ru] || ru));
    });
    document.documentElement.lang = lang;
    localStorage.setItem('lang', lang);
    document.querySelectorAll('.lang-btn').forEach((b) =>
      b.classList.toggle('active', b.dataset.lang === lang));
  }

  // Кнопки переключения
  document.querySelectorAll('.lang-btn').forEach((btn) => {
    btn.addEventListener('click', () => apply(btn.dataset.lang));
  });

  // Применить сохранённый язык (по умолчанию русский)
  apply(localStorage.getItem('lang') || 'ru');
})();
